// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

const WORKSPACE_ROOT = path.resolve(__dirname, '..', '..', '..', '..', '..')

/** The canonical well-known codec map used by the TS matrix runner. */
const TS_CODEC_MAP = new Map<LogicalTypeId, LogicalTypeCodec>([
  ['date-time', dateTimeCodec],
  ['int64', int64Codec],
  ['decimal', decimalCodec],
  ['byte', byteCodec],
  ['uuid', uuidCodec],
  ['number-special', numberSpecialCodec],
])

/** Build a fully-conformant HostManifest for the TS host. */
function conformantTsManifest(): HostManifest {
  return {
    host: 'ts',
    logicalTypeVersion: LOGICAL_TYPE_VERSION,
    supportedIds: [...CANONICAL_IDS],
    deps: {},
  }
}

/** Build a HostManifest with one canonical id missing. */
function manifestMissingId(missingId: LogicalTypeId): HostManifest {
  return {
    host: 'ts-missing',
    logicalTypeVersion: LOGICAL_TYPE_VERSION,
    supportedIds: CANONICAL_IDS.filter((id) => id !== missingId),
    deps: {},
  }
}

/** Build a HostManifest with empty supportedIds (as generated by `nx generate apigen-nx:host`). */
function emptyManifest(host: string): HostManifest {
  return {
    host,
    logicalTypeVersion: LOGICAL_TYPE_VERSION,
    supportedIds: [],
    deps: {},
  }
}

// ---------------------------------------------------------------------------
// 1. checkSupportedIds — ⊇ enforcement
// ---------------------------------------------------------------------------

describe('checkSupportedIds — supportedIds ⊇ canonical', () => {
  it('[conformant] a manifest covering all canonical ids passes (empty errors)', () => {
    const errors = checkSupportedIds(conformantTsManifest())
    expect(errors).toHaveLength(0)
  })

  it('[missing-one] a manifest missing "date-time" produces an error for that id', () => {
    const errors = checkSupportedIds(manifestMissingId('date-time'))
    expect(errors.length).toBeGreaterThan(0)
    expect(errors.some((e) => e.includes('date-time'))).toBe(true)
  })

  it('[missing-one] a manifest missing "int64" produces an error for that id', () => {
    const errors = checkSupportedIds(manifestMissingId('int64'))
    expect(errors.some((e) => e.includes('int64'))).toBe(true)
  })

  it('[missing-all] empty supportedIds produces errors for every canonical id', () => {
    const errors = checkSupportedIds(emptyManifest('new-host'))
    expect(errors.length).toBe(CANONICAL_IDS.length)
    for (const id of CANONICAL_IDS) {
      expect(errors.some((e) => e.includes(id))).toBe(true)
    }
  })

  it('[NEGATIVE teeth] a missing id is actually detected — removing the check makes it pass vacuously', () => {
    // Prove the test would catch a regression: if we supply the FULL id set,
    // the function MUST return 0 errors (not accidentally error on a known id).
    const full = checkSupportedIds(conformantTsManifest())
    expect(full).toHaveLength(0)

    // Now strip one id — must get exactly one error
    const stripped = checkSupportedIds(manifestMissingId('byte'))
    expect(stripped.some((e) => e.includes('byte'))).toBe(true)
  })
})

// ---------------------------------------------------------------------------
// 2. TS matrix — conformant host passes
// ---------------------------------------------------------------------------

describe('runTsMatrix — TS host (conformant)', () => {
  it('[encode] all vectors encode seed to canonical wire', () => {
    const results = runTsMatrix(logicalTypeVectors, TS_CODEC_MAP)
    const encodeResults = results.filter((r) => r.phase === 'encode')
    const failures = encodeResults.filter((r) => !r.pass)
    expect(
      failures,
      `encode failures:\n${failures.map((f) => `  ${f.vectorId}: ${f.error}`).join('\n')}`,
    ).toHaveLength(0)
  })

  it('[invariants] all vectors decode wire and satisfy invariants', () => {
    const results = runTsMatrix(logicalTypeVectors, TS_CODEC_MAP)
    const invResults = results.filter((r) => r.phase === 'invariant')
    const failures = invResults.filter((r) => !r.pass)
    expect(
      failures,
      `invariant failures:\n${failures.map((f) => `  ${f.vectorId}: ${f.error}`).join('\n')}`,
    ).toHaveLength(0)
  })

  it('[negative-controls] all negative controls turn the vector RED', () => {
    const results = runTsMatrix(logicalTypeVectors, TS_CODEC_MAP)
    const ncResults = results.filter((r) => r.phase === 'negative-control')
    const failures = ncResults.filter((r) => !r.pass)
    expect(
      failures,
      `vacuous negative controls:\n${failures.map((f) => `  ${f.vectorId}: ${f.error}`).join('\n')}`,
    ).toHaveLength(0)
  })

  it('[full-pass] all phases pass for all vectors', () => {
    const results = runTsMatrix(logicalTypeVectors, TS_CODEC_MAP)
    const failures = results.filter((r) => !r.pass)
    expect(
      failures,
      `full matrix failures:\n${failures.map((f) => `  ${f.vectorId} (${f.phase}): ${f.error}`).join('\n')}`,
    ).toHaveLength(0)
  })
})

// ---------------------------------------------------------------------------
// 3. TS matrix — host missing a canonical id FAILS
// ---------------------------------------------------------------------------

describe('runTsMatrix — host with missing supportedId', () => {
  it('[TEETH-1] missing "date-time" id in supportedIds triggers a failure', () => {
    // The checkSupportedIds function is the gate — verify it fails.
    const stripped = manifestMissingId('date-time')
    const errors = checkSupportedIds(stripped)
    expect(errors.length).toBeGreaterThan(0)
    expect(errors.some((e) => e.includes('date-time'))).toBe(true)
  })

  it('[TEETH-2] a codec map missing the date-time codec cannot run the vector', () => {
    // Remove one codec from the map to simulate a non-conformant host.
    const partialMap = new Map(TS_CODEC_MAP)
    partialMap.delete('date-time')
    const results = runTsMatrix(logicalTypeVectors, partialMap)
    // The date-time vector must have an encode failure.
    const dtFail = results.find(
      (r) => r.vectorId === 'logical.date-time.roundtrip' && r.phase === 'encode' && !r.pass,
    )
    expect(dtFail, 'expected a failure for missing date-time codec').toBeDefined()
    expect(dtFail!.error).toMatch(/no codec registered/)
  })

  it('[TEETH-3] removing all codecs causes every vector to fail', () => {
    const emptyMap = new Map<LogicalTypeId, LogicalTypeCodec>()
    const results = runTsMatrix(logicalTypeVectors, emptyMap)
    const encodeFailures = results.filter((r) => r.phase === 'encode' && !r.pass)
    expect(encodeFailures.length).toBe(logicalTypeVectors.length)
  })
})

// ---------------------------------------------------------------------------
// 4. Negative-control non-vacuity: the checks actually fail when the codec is broken
// ---------------------------------------------------------------------------

describe('runTsMatrix — negative-control non-vacuity', () => {
  it('[nc-teeth] the negative control for date-time turns red (non-UTC offset wire)', () => {
    const results = runTsMatrix(logicalTypeVectors, TS_CODEC_MAP)
    const ncResult = results.find(
      (r) => r.vectorId === 'logical.date-time.roundtrip' && r.phase === 'negative-control',
    )
    expect(ncResult).toBeDefined()
    // Must be pass=true meaning "the check DID turn red" (the gate verified the mutation is non-conformant)
    expect(ncResult!.pass).toBe(true)
  })

  it('[nc-teeth] the negative control for int64 turns red (numeric wire instead of string)', () => {
    const results = runTsMatrix(logicalTypeVectors, TS_CODEC_MAP)
    const ncResult = results.find(
      (r) => r.vectorId === 'logical.int64.roundtrip' && r.phase === 'negative-control',
    )
    expect(ncResult).toBeDefined()
    expect(ncResult!.pass).toBe(true)
  })

  it('[nc-teeth] the negative control for byte turns red (URL-safe base64 wire)', () => {
    const results = runTsMatrix(logicalTypeVectors, TS_CODEC_MAP)
    const ncResult = results.find(
      (r) => r.vectorId === 'logical.byte.roundtrip' && r.phase === 'negative-control',
    )
    expect(ncResult).toBeDefined()
    expect(ncResult!.pass).toBe(true)
  })

  it('[nc-teeth] the negative control for uuid turns red (uppercase wire)', () => {
    const results = runTsMatrix(logicalTypeVectors, TS_CODEC_MAP)
    const ncResult = results.find(
      (r) => r.vectorId === 'logical.uuid.roundtrip' && r.phase === 'negative-control',
    )
    expect(ncResult).toBeDefined()
    expect(ncResult!.pass).toBe(true)
  })

  it('[nc-teeth] the negative control for NaN turns red (null wire)', () => {
    const results = runTsMatrix(logicalTypeVectors, TS_CODEC_MAP)
    const ncResult = results.find(
      (r) => r.vectorId === 'logical.number-special.nan' && r.phase === 'negative-control',
    )
    expect(ncResult).toBeDefined()
    expect(ncResult!.pass).toBe(true)
  })

  it('[nc-NEGATIVE-vacuous] a vacuous negative control (same wire) is detected', () => {
    // Craft a vector where negativeControl.to === wire — must be flagged as vacuous.
    // We simulate this by running a vector with a mutated negativeControl that maps back to canonical wire.
    // The gate should produce pass=false for that negative-control check.
    const dtVector = logicalTypeVectors.find((v) => v.logicalType === 'date-time')!
    // A mutation that does NOT change the wire (same value = vacuous)
    const vacuousVector: LogicalTypeVector = {
      ...dtVector,
      id: 'test.vacuous-nc',
      negativeControl: {
        mutate: 'wire',
        // Same wire as canonical = vacuous (decoding it still satisfies invariant /epochMs
        // for the exact same timestamp — nothing changes)
        to: dtVector.wire,
      },
    }
    const results = runTsMatrix([vacuousVector], TS_CODEC_MAP)
    const ncResult = results.find(
      (r) => r.vectorId === 'test.vacuous-nc' && r.phase === 'negative-control',
    )
    expect(ncResult).toBeDefined()
    // The negative control is vacuous — the gate should flag it (pass=false)
    expect(ncResult!.pass).toBe(false)
    expect(ncResult!.error).toMatch(/vacuous/)
  })
})

