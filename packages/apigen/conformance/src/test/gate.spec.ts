describe('discoverManifestHosts', () => {
  it('returns [] when packages/apigen/hosts does not exist', () => {
    const manifests = discoverManifestHosts('/tmp/nonexistent-workspace-root')
    expect(manifests).toHaveLength(0)
  })

  it('discovers a host-manifest.json when one is written to the hosts dir', () => {
    // Create a temp workspace with a fake host-manifest.json.
    const tmpDir = fs.mkdtempSync(path.join(os.tmpdir(), 'apigen-gate-test-'))
    const hostsDir = path.join(tmpDir, 'packages', 'apigen', 'hosts', 'my-lang')
    fs.mkdirSync(hostsDir, { recursive: true })

    const fakeManifest: HostManifest = {
      host: 'my-lang',
      logicalTypeVersion: '0.1.0',
      supportedIds: ['date-time', 'int64', 'decimal', 'byte', 'uuid', 'number-special'],
      deps: {},
    }
    fs.writeFileSync(
      path.join(hostsDir, 'host-manifest.json'),
      JSON.stringify(fakeManifest, null, 2),
    )

    const discovered = discoverManifestHosts(tmpDir)
    expect(discovered).toHaveLength(1)
    expect(discovered[0]!.host).toBe('my-lang')
    expect(checkSupportedIds(discovered[0]!)).toHaveLength(0)

    // Cleanup
    fs.rmSync(tmpDir, { recursive: true, force: true })
  })

  it('[TEETH] a generator-scaffolded manifest with empty supportedIds is non-conformant', () => {
    const tmpDir = fs.mkdtempSync(path.join(os.tmpdir(), 'apigen-gate-test-'))
    const hostsDir = path.join(tmpDir, 'packages', 'apigen', 'hosts', 'go')
    fs.mkdirSync(hostsDir, { recursive: true })

    // This is what `nx generate apigen-nx:host` emits: empty supportedIds
    const scaffoldedManifest: HostManifest = {
      host: 'go',
      logicalTypeVersion: '0.1.0',
      supportedIds: [],
      deps: {},
    }
    fs.writeFileSync(
      path.join(hostsDir, 'host-manifest.json'),
      JSON.stringify(scaffoldedManifest, null, 2),
    )

    const discovered = discoverManifestHosts(tmpDir)
    expect(discovered).toHaveLength(1)
    const errors = checkSupportedIds(discovered[0]!)
    // Empty supportedIds → fails for ALL canonical ids
    expect(errors.length).toBe(CANONICAL_IDS.length)

    fs.rmSync(tmpDir, { recursive: true, force: true })
  })
})

describe('discoverHosts', () => {
  it('always includes the TS host', () => {
    const hosts = discoverHosts(WORKSPACE_ROOT)
    expect(hosts.some((h) => h.manifest.host === 'ts')).toBe(true)
  })

  it('includes the Python host when workspace has apigen_logical.py', () => {
    const hosts = discoverHosts(WORKSPACE_ROOT)
    expect(hosts.some((h) => h.manifest.host === 'python')).toBe(true)
  })

  it('TS host runner is "ts"', () => {
    const hosts = discoverHosts(WORKSPACE_ROOT)
    const ts = hosts.find((h) => h.manifest.host === 'ts')
    expect(ts!.runner).toBe('ts')
  })

  it('Python host runner is "python"', () => {
    const hosts = discoverHosts(WORKSPACE_ROOT)
    const py = hosts.find((h) => h.manifest.host === 'python')
    expect(py!.runner).toBe('python')
  })
})

// ---------------------------------------------------------------------------
// 7. Full matrix — integration: TS + Python pass, red host fails gate
// ---------------------------------------------------------------------------

describe('runConformanceMatrix — integration', () => {
  it('[TS-pass] TS host result has passed=true', () => {
    const results = runConformanceMatrix(WORKSPACE_ROOT)
    const ts = results.find((r) => r.host === 'ts')
    expect(ts).toBeDefined()
    const failures = ts!.results.filter((r) => !r.pass)
    expect(
      failures,
      `TS host failures:\n${failures.map((f) => `  ${f.vectorId} (${f.phase}): ${f.error}`).join('\n')}`,
    ).toHaveLength(0)
    expect(ts!.passed).toBe(true)
  })

  it('[Python-pass] Python host result has passed=true', () => {
    const results = runConformanceMatrix(WORKSPACE_ROOT)
    const py = results.find((r) => r.host === 'python')
    expect(py).toBeDefined()
    const failures = py!.results.filter((r) => !r.pass)
    expect(
      failures,
      `Python host failures:\n${failures.map((f) => `  ${f.vectorId} (${f.phase}): ${f.error}`).join('\n')}`,
    ).toHaveLength(0)
    expect(py!.passed).toBe(true)
  })

  it('[red-host TEETH] a red host (missing supportedIds) makes the matrix non-passing', () => {
    // Simulate a failing host by running checkSupportedIds on an empty manifest.
    const redManifest = emptyManifest('go')
    const errors = checkSupportedIds(redManifest)
    expect(errors.length).toBe(CANONICAL_IDS.length)
    // A real runConformanceMatrix call with this host would have passed=false.
    // We verify the logic directly rather than spinning up a fake workspace.
    const anyError = errors.length > 0
    expect(anyError).toBe(true)
  })

  it('[coverage] both TS and Python hosts are discovered and present in results', () => {
    const results = runConformanceMatrix(WORKSPACE_ROOT)
    const hosts = results.map((r) => r.host)
    expect(hosts).toContain('ts')
    expect(hosts).toContain('python')
  })
})
