def run_extractor_tests(results: _Results) -> None:
    """Extract a small inline Python module and verify the Operation shapes."""
    print("\n--- B. Extractor unit tests ---")

    # Write a temp module with known exports.
    src = textwrap.dedent("""\
        from __future__ import annotations
        import asyncio

        def add(x: int, y: int) -> int:
            return x + y

        async def greet(name: str) -> str:
            return f"hello, {name}"

        CONSTANT = 42

        def _private() -> None:
            pass

        class _SkipMe:
            pass
    """)

    with tempfile.NamedTemporaryFile(
        suffix=".py", mode="w", delete=False, prefix="apigen_test_"
    ) as f:
        f.write(src)
        tmp_path = f.name

    try:
        ops = extract_module(tmp_path, namespace="test")

        op_by_id: dict[str, Any] = {}
        for op in ops:
            op_by_id[op["id"]] = op

        # --- host must be 'python' ---
        for op in ops:
            if op.get("host") != "python":
                results.fail(
                    "extractor.host",
                    f"op {op['id']} has host={op.get('host')!r}, expected 'python'",
                )
                break
        else:
            results.ok("extractor.host")

        # --- 'add' should be extracted as kind=action, safe=False ---
        add_id = [k for k in op_by_id if k.endswith("add")]
        if not add_id:
            results.fail("extractor.action.add", "'add' operation not found in extracted ops")
        else:
            add_op = op_by_id[add_id[0]]
            if add_op["kind"] != "action":
                results.fail("extractor.action.add", f"expected kind='action', got {add_op['kind']!r}")
            elif add_op["safe"] is not False:
                results.fail("extractor.action.add", f"expected safe=False for action, got {add_op['safe']!r}")
            elif add_op["async"] is not False:
                results.fail("extractor.action.add", f"expected async=False for sync fn, got {add_op['async']!r}")
            else:
                results.ok("extractor.action.add")

        # --- 'greet' should be async=True ---
        greet_id = [k for k in op_by_id if k.endswith("greet")]
        if not greet_id:
            results.fail("extractor.async.greet", "'greet' operation not found")
        else:
            greet_op = op_by_id[greet_id[0]]
            if greet_op.get("async") is not True:
                results.fail("extractor.async.greet", f"expected async=True, got {greet_op.get('async')!r}")
            else:
                results.ok("extractor.async.greet")

        # --- 'CONSTANT' should be kind=query, safe=True ---
        const_id = [k for k in op_by_id if k.upper().endswith("CONSTANT") or "constant" in k.lower()]
        if not const_id:
            results.fail("extractor.query.constant", "'CONSTANT' operation not found")
        else:
            const_op = op_by_id[const_id[0]]
            if const_op["kind"] != "query":
                results.fail("extractor.query.constant", f"expected kind='query', got {const_op['kind']!r}")
            elif const_op["safe"] is not True:
                results.fail("extractor.query.constant", f"expected safe=True for query, got {const_op['safe']!r}")
            else:
                results.ok("extractor.query.constant")

        # --- '_private' should NOT appear ---
        private_present = any("_private" in k or "private" in k for k in op_by_id)
        if private_present:
            results.fail("extractor.skip.private", "private fn '_private' should not be extracted")
        else:
            results.ok("extractor.skip.private")

        # --- input schema for 'add': properties x and y, both required ---
        if add_id:
            add_op = op_by_id[add_id[0]]
            input_schema = add_op.get("input", {})
            props = input_schema.get("properties", {})
            required = input_schema.get("required", [])
            if "x" in props and "y" in props and "x" in required and "y" in required:
                results.ok("extractor.input_schema.add")
            else:
                results.fail(
                    "extractor.input_schema.add",
                    f"expected props={{x,y}} required=[x,y], got props={list(props)!r} required={required!r}",
                )

        # --- §4 required fields present on all ops ---
        required_fields = {"id", "host", "namespace", "path", "kind", "async", "streaming",
                           "safe", "input", "output", "envelope", "typeText"}
        for op in ops:
            missing = required_fields - set(op.keys())
            if missing:
                results.fail(
                    f"extractor.fields.{op['id']}",
                    f"missing §4 fields: {missing!r}",
                )
            else:
                results.ok(f"extractor.fields.{op.get('id', '?')}")
            break  # just check the first op; individual field tests above cover the rest

    finally:
        os.unlink(tmp_path)


