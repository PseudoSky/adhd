class _Results:
    def __init__(self) -> None:
        self._pass: list[str] = []
        self._fail: list[tuple[str, str]] = []

    def ok(self, test_id: str) -> None:
        self._pass.append(test_id)
        print(f"  PASS  {test_id}")

    def fail(self, test_id: str, reason: str) -> None:
        self._fail.append((test_id, reason))
        print(f"  FAIL  {test_id}: {reason}")

    def summary(self) -> int:
        total = len(self._pass) + len(self._fail)
        print(f"\n{'='*60}")
        print(f"Results: {len(self._pass)}/{total} passed, {len(self._fail)} failed")
        if self._fail:
            print("\nFailed tests:")
            for tid, msg in self._fail:
                print(f"  - {tid}: {msg}")
        return 1 if self._fail else 0


