def _run_deliberate_failure_demo() -> None:
    """Standalone smoke test: prove the runner exits 1 when a test fails.

    Called with --self-test flag. Runs one deliberate failure and verifies
    the exit code is 1. This is not part of the normal test suite.
    """
    results = _Results()
    results.fail("deliberate.failure", "this is intentional — proving exit non-zero")
    code = results.summary()
    sys.exit(code)


