def _deep_equal(a: Any, b: Any) -> bool:
    """JSON-level deep equality (order-insensitive for dicts, order-sensitive for lists)."""
    return json.dumps(a, sort_keys=True) == json.dumps(b, sort_keys=True)


def run_conformance_vectors(results: _Results, vectors: dict[str, Any]) -> None:
    """Run all five conformance categories (A–E) from the fixture."""
    print("\n--- A. Descriptor round-trip ---")
    for v in vectors.get("DESCRIPTOR_VECTORS", []):
        vid = v["id"]
        op = v["input"]
        # Round-trip: JSON.dumps → JSON.loads must be deep-equal.
        rt = json.loads(json.dumps(op))
        if _deep_equal(op, rt):
            results.ok(vid)
        else:
            results.fail(vid, f"round-trip differs: {op!r} → {rt!r}")

    print("\n--- B. Naming / verb from safe ---")
    for v in vectors.get("NAMING_VECTORS", []):
        vid = v["id"]
        expected = v.get("expected", {})

        # Verb-from-safe vectors (single op).
        if "op" in v:
            op = v["op"]
            safe = op.get("safe", False)
            actual_verb = "GET" if safe else "POST"

            if "httpVerb" in expected:
                if actual_verb == expected["httpVerb"]:
                    results.ok(vid)
                else:
                    results.fail(vid, f"expected httpVerb {expected['httpVerb']!r}, got {actual_verb!r}")
            elif "httpVerbIsNot" in expected:
                if actual_verb != expected["httpVerbIsNot"]:
                    results.ok(vid)
                else:
                    results.fail(vid, f"expected verb NOT to be {expected['httpVerbIsNot']!r}")

        # Collision vectors (list of ops).
        elif "ops" in v:
            ops = v["ops"]
            collision_expected = expected.get("collision", False)

            # Collision check: two ops collide iff they have the same path words
            # in every transport projection. Simplified check: identical namespace
            # + path words across distinct ids.
            def _op_projection_key(op: dict[str, Any]) -> str:
                ns_words = "_".join(op["namespace"]["words"])
                path_words = "_".join(
                    "_".join(seg["words"]) for seg in op["path"]
                )
                safe_str = "GET" if op.get("safe") else "POST"
                return f"{ns_words}/{path_words}:{safe_str}"

            keys = [_op_projection_key(o) for o in ops]
            ids = [o["id"] for o in ops]
            has_collision = len(set(keys)) < len(keys) and len(set(ids)) == len(ids)

            if collision_expected == has_collision:
                results.ok(vid)
            else:
                results.fail(
                    vid,
                    f"collision={has_collision}, expected={collision_expected}. "
                    f"keys={keys}, ids={ids}",
                )

    print("\n--- C. Envelope binding ---")
    for v in vectors.get("ENVELOPE_VECTORS", []):
        vid = v["id"]
        plugin_id: str = v["pluginId"]
        field: str = v["field"]
        expected = v["expected"]

        # Reproduce the @adhd/apigen-naming logic in Python.
        def _envelope_key(pid: str, f: str) -> str:
            if pid == "adhd":
                return f"x-adhd-{f}"
            return f"x-{pid}-{f}"

        def _envelope_cli_flag(pid: str, f: str) -> str:
            if pid == "adhd":
                return f"--adhd-{f}"
            return f"--{pid}-{f}"

        def _envelope_env_var(pid: str, f: str) -> str:
            if pid == "adhd":
                return f"APIGEN_{f.upper().replace('-', '_')}"
            return f"APIGEN_{pid.upper().replace('-', '_')}_{f.upper().replace('-', '_')}"

        actual_key  = _envelope_key(plugin_id, field)
        actual_flag = _envelope_cli_flag(plugin_id, field)
        actual_env  = _envelope_env_var(plugin_id, field)

        errors: list[str] = []
        if actual_key != expected["httpHeader"]:
            errors.append(f"httpHeader: got {actual_key!r}, want {expected['httpHeader']!r}")
        if actual_key != expected["grpcMeta"]:
            errors.append(f"grpcMeta: got {actual_key!r}, want {expected['grpcMeta']!r}")
        if actual_key != expected["mcpMetaKey"]:
            errors.append(f"mcpMetaKey: got {actual_key!r}, want {expected['mcpMetaKey']!r}")
        if actual_flag != expected["cliFlag"]:
            errors.append(f"cliFlag: got {actual_flag!r}, want {expected['cliFlag']!r}")
        if actual_env != expected["cliEnvVar"]:
            errors.append(f"cliEnvVar: got {actual_env!r}, want {expected['cliEnvVar']!r}")

        if errors:
            results.fail(vid, "; ".join(errors))
        else:
            results.ok(vid)

    print("\n--- D. Error mapping ---")
    for v in vectors.get("ERROR_MAPPING_VECTORS", []):
        vid = v["id"]
        code: str = v["code"]  # type: ignore[assignment]
        expected = v["expected"]

        errors: list[str] = []
        if HTTP_STATUS.get(code) != expected["http"]:  # type: ignore[arg-type]
            errors.append(f"HTTP_STATUS[{code}]: got {HTTP_STATUS.get(code)}, want {expected['http']}")
        if GRPC_CODE.get(code) != expected["grpc"]:  # type: ignore[arg-type]
            errors.append(f"GRPC_CODE[{code}]: got {GRPC_CODE.get(code)!r}, want {expected['grpc']!r}")
        if CLI_EXIT_CODE.get(code) != expected["cli"]:  # type: ignore[arg-type]
            errors.append(f"CLI_EXIT_CODE[{code}]: got {CLI_EXIT_CODE.get(code)}, want {expected['cli']}")
        if MCP_ERROR_KIND.get(code) != expected["mcp"]:  # type: ignore[arg-type]
            errors.append(f"MCP_ERROR_KIND[{code}]: got {MCP_ERROR_KIND.get(code)!r}, want {expected['mcp']!r}")

        # Also check code survives serialisation (ApiError.to_json()).
        err_obj = ApiError(code, "test")  # type: ignore[arg-type]
        as_json = err_obj.to_json()
        if as_json.get("code") != code:
            errors.append(f"to_json().code: got {as_json.get('code')!r}, want {code!r}")

        if errors:
            results.fail(vid, "; ".join(errors))
        else:
            results.ok(vid)
            # Also emit a separate .serialize pass result.
            results.ok(f"{vid}.serialize")

    print("\n--- E. Validation necessary-not-sufficient ---")
    for v in vectors.get("VALIDATION_VECTORS", []):
        vid = v["id"]
        schema = v["schema"]
        schema_valid_value = v["schemaValidValue"]

        # The validator MUST accept the schema-valid value.
        accepted = is_valid(schema, schema_valid_value)
        if accepted:
            results.ok(vid)
        else:
            results.fail(
                vid,
                f"schema validator rejected a schema-valid value (should have accepted): "
                f"{schema_valid_value!r}",
            )

        # Negative control: the validator MUST reject a clearly invalid value.
        neg_vid = f"{vid}.NEGATIVE"
        rejected = not is_valid(schema, "not-an-object")
        if rejected:
            results.ok(neg_vid)
        else:
            results.fail(
                neg_vid,
                f"schema validator accepted 'not-an-object' as valid for schema {schema!r}",
            )


