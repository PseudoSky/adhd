#!/usr/bin/env python3
"""
run_tests.py — Self-contained test runner for the apigen Python host.

Guard: python3 packages/apigen/python/run_tests.py → exit 0 on full pass.
       Non-zero exit if ANY test case fails.

Test categories:
    A. Conformance vectors — descriptor round-trip, naming/verb, collision, envelope,
       error-mapping, validation (consumed from the canonical JSON fixture generated
       by @adhd/apigen-conformance; never hand-authored to avoid drift).
    B. Extractor unit tests — extract a small inline Python module → check Operation shape.
    C. Runtime unit tests  — validate → dispatch → result; validation failure path.
    D. Echo round-trip     — echo plugin through runtime, proving the full path works.

The fixture at conformance_vectors.json is generated by:
    node -e "const c=require('...dist/.../conformance/index.js'); ..." > conformance_vectors.json

If the fixture is missing, the runner regenerates it via node before proceeding.
"""

