FIXTURE_PATH = SCRIPT_DIR / "conformance_vectors.json"
DIST_CONFORMANCE = (
    SCRIPT_DIR.parents[3] / "dist" / "packages" / "apigen" / "conformance" / "index.js"
)


def _ensure_fixture() -> dict[str, Any]:
    """Load or (re)generate the conformance_vectors.json fixture.

    Strategy:
    1. If the file exists and is non-empty, load it directly.
    2. Otherwise, call node to dump the vectors from the built dist package.
    3. If node/dist is unavailable, fail loudly (no silent drift).
    """
    if FIXTURE_PATH.exists() and FIXTURE_PATH.stat().st_size > 0:
        with open(FIXTURE_PATH) as f:
            return json.load(f)

    # Try to regenerate.
    if not DIST_CONFORMANCE.exists():
        print(
            f"ERROR: Conformance vectors fixture not found at {FIXTURE_PATH}\n"
            f"       and dist package not found at {DIST_CONFORMANCE}.\n"
            f"       Run: npx nx build apigen-conformance  (from repo root)",
            file=sys.stderr,
        )
        sys.exit(1)

    script = (
        "const c=require(%s);"
        "process.stdout.write(JSON.stringify({"
        "DESCRIPTOR_VECTORS:c.DESCRIPTOR_VECTORS,"
        "NAMING_VECTORS:c.NAMING_VECTORS,"
        "ENVELOPE_VECTORS:c.ENVELOPE_VECTORS,"
        "ERROR_MAPPING_VECTORS:c.ERROR_MAPPING_VECTORS,"
        "VALIDATION_VECTORS:c.VALIDATION_VECTORS,"
        "ENVELOPE_CASES:c.ENVELOPE_CASES"
        "},null,2));"
    ) % json.dumps(str(DIST_CONFORMANCE))

    result = subprocess.run(
        ["node", "-e", script],
        capture_output=True,
        text=True,
        check=False,
    )
    if result.returncode != 0:
        print(f"ERROR: node failed to dump conformance vectors:\n{result.stderr}", file=sys.stderr)
        sys.exit(1)

    data = json.loads(result.stdout)
    with open(FIXTURE_PATH, "w") as f:
        json.dump(data, f, indent=2)
    return data


