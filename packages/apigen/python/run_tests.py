def run_runtime_tests(results: _Results) -> None:
    """Test validation + dispatch through the Runtime."""
    print("\n--- C. Runtime unit tests ---")

    def multiply(a: int, b: int) -> int:
        return a * b

    def greet(ctx: dict, name: str) -> str:
        session = ctx.get("session", "anonymous")
        return f"hello {name} ({session})"

    registry = {
        "math/multiply": multiply,
        "auth/greet": greet,
    }
    rt = Runtime(registry)

    # --- successful dispatch ---
    op_mul = {
        "id": "math/multiply",
        "host": "python",
        "input": {
            "type": "object",
            "properties": {"a": {"type": "integer"}, "b": {"type": "integer"}},
            "required": ["a", "b"],
        },
        "output": {"type": "integer"},
        "envelope": {},
    }
    req = HostRequest(operation=op_mul, data={"a": 6, "b": 7}, envelope={}, transport="http")
    try:
        result = rt.invoke_sync(req)
        if result == 42:
            results.ok("runtime.dispatch.multiply")
        else:
            results.fail("runtime.dispatch.multiply", f"expected 42, got {result!r}")
    except Exception as exc:
        results.fail("runtime.dispatch.multiply", f"unexpected exception: {exc}")

    # --- ctx parameter receives envelope ---
    op_greet = {
        "id": "auth/greet",
        "host": "python",
        "input": {
            "type": "object",
            "properties": {"name": {"type": "string"}},
            "required": ["name"],
        },
        "output": {"type": "string"},
        "envelope": {},
    }
    req_greet = HostRequest(
        operation=op_greet,
        data={"name": "Alice"},
        envelope={"session": "sess-123"},
        transport="http",
    )
    try:
        result = rt.invoke_sync(req_greet)
        if result == "hello Alice (sess-123)":
            results.ok("runtime.ctx.greet")
        else:
            results.fail("runtime.ctx.greet", f"got {result!r}")
    except Exception as exc:
        results.fail("runtime.ctx.greet", f"unexpected exception: {exc}")

    # --- validation failure → ApiError(invalid_argument) ---
    req_bad = HostRequest(
        operation=op_mul,
        data={"a": "not-an-int"},  # missing b, wrong type for a
        envelope={},
        transport="http",
    )
    try:
        rt.invoke_sync(req_bad)
        results.fail("runtime.validation_failure", "expected ApiError, got no exception")
    except ApiError as exc:
        if exc.code == "invalid_argument":
            results.ok("runtime.validation_failure")
        else:
            results.fail("runtime.validation_failure", f"expected invalid_argument, got {exc.code!r}")
    except Exception as exc:
        results.fail("runtime.validation_failure", f"unexpected exception type: {type(exc).__name__}: {exc}")

    # --- not-found → ApiError(not_found) ---
    op_missing = {
        "id": "missing/op",
        "host": "python",
        "input": {"type": "object", "properties": {}},
        "output": {},
        "envelope": {},
    }
    req_missing = HostRequest(operation=op_missing, data={}, envelope={}, transport="http")
    try:
        rt.invoke_sync(req_missing)
        results.fail("runtime.not_found", "expected ApiError(not_found), got no exception")
    except ApiError as exc:
        if exc.code == "not_found":
            results.ok("runtime.not_found")
        else:
            results.fail("runtime.not_found", f"expected not_found, got {exc.code!r}")
    except Exception as exc:
        results.fail("runtime.not_found", f"unexpected exception type: {type(exc).__name__}: {exc}")

    # --- internal error wrapping ---
    def boom() -> None:
        raise ValueError("something went wrong")

    registry_boom = {"boom/op": boom}
    rt_boom = Runtime(registry_boom)
    op_boom = {
        "id": "boom/op",
        "host": "python",
        "input": {"type": "object", "properties": {}},
        "output": {},
        "envelope": {},
    }
    req_boom = HostRequest(operation=op_boom, data={}, envelope={}, transport="http")
    try:
        rt_boom.invoke_sync(req_boom)
        results.fail("runtime.internal_error", "expected ApiError(internal), got no exception")
    except ApiError as exc:
        if exc.code == "internal":
            results.ok("runtime.internal_error")
        else:
            results.fail("runtime.internal_error", f"expected internal, got {exc.code!r}")
    except Exception as exc:
        results.fail("runtime.internal_error", f"unexpected exception type: {type(exc).__name__}: {exc}")


